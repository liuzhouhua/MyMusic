package com.example.mymusic.model;

public class Music {

	private String mMusicName;
	private String mMusicSinger;
	private String mMusicAlbum;
	private String mMusicUrl;
	private String mLetterofSinger;
	private String mLetterofAlbum;
	public String getmMusicName() {
		return mMusicName;
	}
	public void setmMusicName(String mMusicName) {
		this.mMusicName = mMusicName;
	}
	public String getmMusicSinger() {
		return mMusicSinger;
	}
	public void setmMusicSinger(String mMusicSinger) {
		this.mMusicSinger = mMusicSinger;
	}
	public String getmMusicAlbum() {
		return mMusicAlbum;
	}
	public void setmMusicAlbum(String mMusicAlbum) {
		this.mMusicAlbum = mMusicAlbum;
	}
	public String getmMusicUrl() {
		return mMusicUrl;
	}
	public void setmMusicUrl(String mMusicUrl) {
		this.mMusicUrl = mMusicUrl;
	}
	public String getmLetterofSinger() {
		return mLetterofSinger;
	}
	public void setmLetterofSinger(String mLetterofSinger) {
		this.mLetterofSinger = mLetterofSinger;
	}
	public String getmLetterofAlbum() {
		return mLetterofAlbum;
	}
	public void setmLetterofAlbum(String mLetterofAlbum) {
		this.mLetterofAlbum = mLetterofAlbum;
	}
	
}
