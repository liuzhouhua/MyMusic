package com.example.mymusic.model;

public class Singer {

	private String singer;
	
	private int count;
	
	private String letterForSinger;

	public String getSinger() {
		return singer;
	}

	public void setSinger(String singer) {
		this.singer = singer;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getLetterForSinger() {
		return letterForSinger;
	}

	public void setLetterForSinger(String letterForSinger) {
		this.letterForSinger = letterForSinger;
	}

}
